package modelo.enums;

/**
 * Enumeração que define os tamanhos disponíveis para produtos.
 *
 * <p>
 * Representa as categorias de tamanho que podem ser atribuídas aos produtos no
 * sistema.</p>
 */
public enum Tamanho {
    /**
     * Tamanho pequeno
     */
    PEQUENO,
    /**
     * Tamanho médio
     */
    MEDIO,
    /**
     * Tamanho grande
     */
    GRANDE;
}
